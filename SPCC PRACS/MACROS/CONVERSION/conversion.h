#define ctof(a) ((a * 9/5)+ 32) /*Celcius to fahrenheit*/
#define ftoc(a) ((a - 32) * 5/9) /* fahrenheit to Celcius*/
#define mtof(a) (a * 3.281)  /*meter to feet*/
#define ftom(a) (a / 3.281) /*feet to meter*/
#define ltocu(a) (a / 28.317) /* litre to cubic meter*/
#define cutol(a) (a * 28.317) /* cubic meter to liter*/